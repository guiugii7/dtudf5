<?php
// Создать ассоциативный массив с информацией о товарах (название товара и количество на складе)
$productos = [
    "Молоко" => 15,
    "Яйца" => 20,
    "Хлеб" => 10,
    "Сыр" => 5,
    "Колбаса" => 7
];

// Найти товары, количество которых меньше 10 штук
$productos_escasos = [];
foreach ($productos as $producto => $cantidad) {
    if ($cantidad < 10) {
        $productos_escasos[] = $producto;
    }
}

// Вывести результат
echo "Товары, количество которых меньше 10 штук: " . implode(", ", $productos_escasos);


?>